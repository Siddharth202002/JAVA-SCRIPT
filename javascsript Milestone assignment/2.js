let num1 = 10;
let num2 = 2;
let operator = "/";
switch (operator) {
  case "+":
    console.log("The sum is " + (num1 + num2));
    break;

  case "-":
    console.log("The subtract is " + (num1 - num2));
    break;

  case "*":
    console.log("The product is " + (num1 * num2));
    break;
  case "/":
    console.log("The division is " + num1 / num2);
    break;
}
